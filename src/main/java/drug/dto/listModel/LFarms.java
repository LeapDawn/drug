package drug.dto.listModel;

public class LFarms {
	private int page;
	private int rows;
	private String sort;
	private String order;

	private String farmname;

	private String farmaddress;

	private String farmphone;

	private String farmlinkman;

	private String farmraisetype;

	private String farmprovince;

	public int getPage() {
		return page;
	}

	public void setPage(int page) {
		this.page = page;
	}

	public int getRows() {
		return rows;
	}

	public void setRows(int rows) {
		this.rows = rows;
	}

	public String getSort() {
		return sort;
	}

	public void setSort(String sort) {
		this.sort = sort;
	}

	public String getOrder() {
		return order;
	}

	public void setOrder(String order) {
		this.order = order;
	}

	public String getFarmname() {
		return farmname;
	}

	public void setFarmname(String farmname) {
		this.farmname = farmname;
	}

	public String getFarmaddress() {
		return farmaddress;
	}

	public void setFarmaddress(String farmaddress) {
		this.farmaddress = farmaddress;
	}

	public String getFarmphone() {
		return farmphone;
	}

	public void setFarmphone(String farmphone) {
		this.farmphone = farmphone;
	}

	public String getFarmlinkman() {
		return farmlinkman;
	}

	public void setFarmlinkman(String farmlinkman) {
		this.farmlinkman = farmlinkman;
	}

	public String getFarmraisetype() {
		return farmraisetype;
	}

	public void setFarmraisetype(String farmraisetype) {
		this.farmraisetype = farmraisetype;
	}

	public String getFarmprovince() {
		return farmprovince;
	}

	public void setFarmprovince(String farmprovince) {
		this.farmprovince = farmprovince;
	}
}
